
def funcsum(num1, num2):
    """the function return the SUM of numbers"""
    return num1+num2

def funcmul(num1, num2):
    """the function return the MUL of numbers"""
    return num1*num2

def main(): # Call the function func
    print(funcsum(5, 5))
    print(funcmul(5, 5))

if __name__ == "__main__":
    main()